export function getAreaBySlug(slug: string): AreaContent | undefined {
  return areaContents.find((area) => area.slug === slug);
}

export function getAllAreaSlugs(): string[] {
  return areaContents.map((area) => area.slug);
}

/** 구 이름으로 상권 페이지 찾기 (동 페이지 → 상권 페이지 내부 링크용) */
export function getAreaByDistrict(district: string): AreaContent | undefined {
  return areaContents.find((area) => area.district === district);
}
